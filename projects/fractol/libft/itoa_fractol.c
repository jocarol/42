#include "libft.h"

char		  *itoa_fractol(uintmax_t n)
{
	static  char	str[10];
  char          *pt;

  pt = &str[9];
	while (n)
	{
		*pt = '0' + n % 10;
		n /= 10;
    --pt;
	}
	return (pt);
}

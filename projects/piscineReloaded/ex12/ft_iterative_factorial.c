int	ft_iterative_factorial(int nb)
{
	int	res;
	int	i;

	if (nb < 0 || nb > 12)
		return (0);
	res = 1;
	i = 1;
	while (i <= nb)
	{
		res *= i;
		i++;
	}
	return (res);
}
